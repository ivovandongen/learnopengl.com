#include <file.hpp>
#include <image.hpp>
#include <program.hpp>
#include <texture.hpp>
#include <camera.hpp>
#include <model.hpp>

#include <glad/glad.h>
#include <GLFW/glfw3.h>
#include <glm/glm.hpp>
#include <glm/gtc/matrix_transform.hpp>

#include <cmath>
#include <iostream>
#include <map>


unsigned int width = 640, height = 480;

// Timekeeping
float deltaTime = 0.0f; // Time between current frame and last frame
float lastFrame = 0.0f; // Time of last frame

// Camera
Camera camera{glm::vec3(0.0f, 0.0f, 3.0f)};

// Mouse
bool firstMouse = true;
double lastX = width / 2.0;
double lastY = height / 2.0;

// Called every frame
void handleKeyCameraControls(GLFWwindow *window) {
    if (glfwGetKey(window, GLFW_KEY_W) == GLFW_PRESS) {
        camera.processKeyboard(CameraMovement::FORWARD, deltaTime);
    }
    if (glfwGetKey(window, GLFW_KEY_S) == GLFW_PRESS) {
        camera.processKeyboard(CameraMovement::BACKWARD, deltaTime);
    }
    if (glfwGetKey(window, GLFW_KEY_A) == GLFW_PRESS) {
        camera.processKeyboard(CameraMovement::LEFT, deltaTime);
    }

    if (glfwGetKey(window, GLFW_KEY_D) == GLFW_PRESS) {
        camera.processKeyboard(CameraMovement::RIGHT, deltaTime);
    }
}

void mouseCallback(GLFWwindow *, double xpos, double ypos) {
    if (firstMouse) {
        lastX = xpos;
        lastY = ypos;
        firstMouse = false;
    }

    double xoffset = xpos - lastX;
    double yoffset = lastY - ypos;
    lastX = xpos;
    lastY = ypos;

    camera.processMouseMovement(xoffset, yoffset);
}

void mouseScrollCallback(GLFWwindow *, double, double yoffset) {
    camera.processMouseScroll(yoffset);
}

int main() {

    if (!glfwInit()) {
        // Initialization failed
        std::cout << "Failed to initialize GLFW" << std::endl;
        return -1;
    }

    glfwWindowHint(GLFW_RESIZABLE, GLFW_TRUE);

    // Specify OpenGL version to use
    glfwWindowHint(GLFW_OPENGL_DEBUG_CONTEXT, GLFW_TRUE);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
    glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);
#ifdef __APPLE__
    glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);
#endif


    GLFWwindow *window = glfwCreateWindow(width, height, "OpenGL Tutorial", nullptr, nullptr);
    if (!window) {
        // Window or OpenGL context creation failed
        std::cout << "Failed to create GLFW window" << std::endl;
        return -1;
    }

    // Position window in the center
    const GLFWvidmode *videoMode = glfwGetVideoMode(glfwGetPrimaryMonitor());
    glfwSetWindowPos(window, (videoMode->width - width) / 2, (videoMode->height - height) / 2);

    // Make the context current for this thread
    glfwMakeContextCurrent(window);

    // Load glad
    if (!gladLoadGL()) {
        std::cout << "Could not initialize GLAD" << std::endl;
        return -1;
    }

    // Resize gl viewport on window resize
    glfwSetFramebufferSizeCallback(window, [](GLFWwindow *, int width, int height) {
        glViewport(0, 0, width, height);
    });

    // Handle some input (called less frequently)
    glfwSetKeyCallback(window, [](GLFWwindow *window, int key, int, int action, int) {
        if (key == GLFW_KEY_ESCAPE && action == GLFW_PRESS) {
            glfwSetWindowShouldClose(window, GLFW_TRUE);
            return;
        }
    });

    // Disable mouse cursor
    glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);

    // Set mouse callbacks
    glfwSetCursorPosCallback(window, mouseCallback);
    glfwSetScrollCallback(window, mouseScrollCallback);

    // Enable vsync
    glfwSwapInterval(1);


    // Print gl version
    auto version = glGetString(GL_VERSION);
    std::cout << "Using OpenGL version: " << version << std::endl;

    glEnable(GL_DEPTH_TEST);

    Program modelProgram(readFile("basic.vertex.glsl"), readFile("basic.fragment.glsl"));
    Program skyboxProgram(readFile("skybox.vertex.glsl"), readFile("skybox.fragment.glsl"));

    float skyboxVertices[] = {
            // positions
            -1.0f, 1.0f, -1.0f,
            -1.0f, -1.0f, -1.0f,
            1.0f, -1.0f, -1.0f,
            1.0f, -1.0f, -1.0f,
            1.0f, 1.0f, -1.0f,
            -1.0f, 1.0f, -1.0f,

            -1.0f, -1.0f, 1.0f,
            -1.0f, -1.0f, -1.0f,
            -1.0f, 1.0f, -1.0f,
            -1.0f, 1.0f, -1.0f,
            -1.0f, 1.0f, 1.0f,
            -1.0f, -1.0f, 1.0f,

            1.0f, -1.0f, -1.0f,
            1.0f, -1.0f, 1.0f,
            1.0f, 1.0f, 1.0f,
            1.0f, 1.0f, 1.0f,
            1.0f, 1.0f, -1.0f,
            1.0f, -1.0f, -1.0f,

            -1.0f, -1.0f, 1.0f,
            -1.0f, 1.0f, 1.0f,
            1.0f, 1.0f, 1.0f,
            1.0f, 1.0f, 1.0f,
            1.0f, -1.0f, 1.0f,
            -1.0f, -1.0f, 1.0f,

            -1.0f, 1.0f, -1.0f,
            1.0f, 1.0f, -1.0f,
            1.0f, 1.0f, 1.0f,
            1.0f, 1.0f, 1.0f,
            -1.0f, 1.0f, 1.0f,
            -1.0f, 1.0f, -1.0f,

            -1.0f, -1.0f, -1.0f,
            -1.0f, -1.0f, 1.0f,
            1.0f, -1.0f, -1.0f,
            1.0f, -1.0f, -1.0f,
            -1.0f, -1.0f, 1.0f,
            1.0f, -1.0f, 1.0f
    };

    // skybox VAO
    unsigned int skyboxVao, skyboxVbo;
    glGenVertexArrays(1, &skyboxVao);
    glGenBuffers(1, &skyboxVbo);
    glBindVertexArray(skyboxVao);
    glBindBuffer(GL_ARRAY_BUFFER, skyboxVbo);
    glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void *) nullptr);

    Texture skyboxTexture{{
                                  Image{"resources/skybox/right.jpg", false},
                                  Image{"resources/skybox/left.jpg", false},
                                  Image{"resources/skybox/top.jpg", false},
                                  Image{"resources/skybox/bottom.jpg", false},
                                  Image{"resources/skybox/front.jpg", false},
                                  Image{"resources/skybox/back.jpg", false}
                          }};

    Model model{"resources/nanosuit/nanosuit.obj"};


    modelProgram.bind();
    modelProgram.setUniform("skybox", 0);

    skyboxProgram.bind();
    skyboxProgram.setUniform("skybox", 0);


    while (!glfwWindowShouldClose(window)) {
        // Update time
        float currentFrame = glfwGetTime();
        deltaTime = currentFrame - lastFrame;
        lastFrame = currentFrame;

        // Handle camera controls
        handleKeyCameraControls(window);

        // Check errors
        int error;
        while ((error = glGetError())) {
            std::cout << "GL error: " << error << std::endl;
            glfwSetWindowShouldClose(window, GLFW_TRUE);
        }

        glEnable(GL_DEPTH_TEST);

        glClearColor(0.1f, 0.1f, 0.1f, 1.0f);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

        glm::mat4 view = camera.viewMatrix();
        glm::mat4 projection = glm::perspective(glm::radians(camera.zoom()), (float) width / (float) height, 0.1f,
                                                100.0f);

        // Draw model
        {
            modelProgram.bind();
            glm::mat4 modelM = glm::mat4(1.0f);
            modelM = glm::translate(modelM, glm::vec3(0.0f, -1.75f, 0.0f)); // translate it down so it's at the center of the scene
            modelM = glm::scale(modelM, glm::vec3(0.2f, 0.2f, 0.2f));	// it's a bit too big for our scene, so scale it down
            modelProgram.setUniform("model", modelM);
            modelProgram.setUniform("view", view);
            modelProgram.setUniform("projection", projection);
            modelProgram.setUniform("cameraPos", camera.position());

            glActiveTexture(GL_TEXTURE0);
            skyboxTexture.bind();
            model.draw(modelProgram);
        }

        // Draw skybox
        {
            glDepthFunc(GL_LEQUAL);
            skyboxProgram.bind();
            view = glm::mat4(glm::mat3(camera.viewMatrix())); // remove translation from the view matrix
            skyboxProgram.setUniform("view", view);
            skyboxProgram.setUniform("projection", projection);

            glBindVertexArray(skyboxVao);
            glActiveTexture(GL_TEXTURE0);
            skyboxTexture.bind();
            glDrawArrays(GL_TRIANGLES, 0, 36);
            glDepthFunc(GL_LESS);
        }

        glfwSwapBuffers(window);
        glfwPollEvents();
    }

    glfwDestroyWindow(window);
    glfwTerminate();
}
