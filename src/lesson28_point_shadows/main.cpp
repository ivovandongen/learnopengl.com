void renderScene(const Program &program);

void renderCube();

unsigned int SHADOW_WIDTH = 1024, SHADOW_HEIGHT = 1024;
unsigned int width = 1024, height = 768;

// Timekeeping
float deltaTime = 0.0f; // Time between current frame and last frame
float lastFrame = 0.0f; // Time of last frame

// Camera
Camera camera{{0.0f, 0.0f, 3.0f}};

// Mouse
bool firstMouse = true;
double lastX = width / 2.0;
double lastY = height / 2.0;

// Called every frame
void handleKeyCameraControls(GLFWwindow *window) {
    if (glfwGetKey(window, GLFW_KEY_W) == GLFW_PRESS) {
        camera.processKeyboard(CameraMovement::FORWARD, deltaTime);
    }
    if (glfwGetKey(window, GLFW_KEY_S) == GLFW_PRESS) {
        camera.processKeyboard(CameraMovement::BACKWARD, deltaTime);
    }
    if (glfwGetKey(window, GLFW_KEY_A) == GLFW_PRESS) {
        camera.processKeyboard(CameraMovement::LEFT, deltaTime);
    }

    if (glfwGetKey(window, GLFW_KEY_D) == GLFW_PRESS) {
        camera.processKeyboard(CameraMovement::RIGHT, deltaTime);
    }
}

void mouseCallback(GLFWwindow *, double xpos, double ypos) {
    if (firstMouse) {
        lastX = xpos;
        lastY = ypos;
        firstMouse = false;
    }

    double xoffset = xpos - lastX;
    double yoffset = lastY - ypos;
    lastX = xpos;
    lastY = ypos;

    camera.processMouseMovement(xoffset, yoffset);
}

void mouseScrollCallback(GLFWwindow *, double, double yoffset) {
    camera.processMouseScroll(yoffset);
}

bool shadows = false;

int main() {

    if (!glfwInit()) {
        // Initialization failed
        std::cout << "Failed to initialize GLFW" << std::endl;
        return -1;
    }

    glfwWindowHint(GLFW_RESIZABLE, GLFW_TRUE);

    // Specify OpenGL version to use
    glfwWindowHint(GLFW_OPENGL_DEBUG_CONTEXT, GLFW_TRUE);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
    glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);
#ifdef __APPLE__
    glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);
#endif

    GLFWwindow *window = glfwCreateWindow(width, height, "OpenGL Tutorial", nullptr, nullptr);
    if (!window) {
        // Window or OpenGL context creation failed
        std::cout << "Failed to create GLFW window" << std::endl;
        return -1;
    }

    // Position window in the center
    const GLFWvidmode *videoMode = glfwGetVideoMode(glfwGetPrimaryMonitor());
    glfwSetWindowPos(window, (videoMode->width - width) / 2, (videoMode->height - height) / 2);

    // Make the context current for this thread
    glfwMakeContextCurrent(window);

    // Load glad
    if (!gladLoadGLLoader((GLADloadproc) glfwGetProcAddress)) {
        std::cout << "Could not initialize GLAD" << std::endl;
        return -1;
    }

    // Resize gl viewport on window resize
    glfwSetFramebufferSizeCallback(window, [](GLFWwindow *, int width, int height) {
        glViewport(0, 0, width, height);
    });

    // Handle some input (called less frequently)
    glfwSetKeyCallback(window, [](GLFWwindow *window, int key, int, int action, int) {
        if (key == GLFW_KEY_ESCAPE && action == GLFW_PRESS) {
            glfwSetWindowShouldClose(window, GLFW_TRUE);
            return;
        }

        if (key == GLFW_KEY_SPACE && action == GLFW_PRESS) {
            shadows = !shadows;
            std::cout << "Shadows " << (shadows ? "ON" : "OFF") << std::endl;
            return;
        }
    });

    // Disable mouse cursor
    glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);

    // Set mouse callbacks
    glfwSetCursorPosCallback(window, mouseCallback);
    glfwSetScrollCallback(window, mouseScrollCallback);

    // Enable vsync
    glfwSwapInterval(1);

    // Print gl version
    auto version = glGetString(GL_VERSION);
    std::cout << "Using OpenGL version: " << version << std::endl;

    glEnable(GL_DEPTH_TEST);
    glEnable(GL_CULL_FACE);

    Program depthMapProgram(
            readFile("depth_map.vertex.glsl"),
            readFile("depth_map.geometry.glsl"),
            readFile("depth_map.fragment.glsl")
    );

    Program lightProgram(
            readFile("light.vertex.glsl"),
            readFile("light.fragment.glsl")
    );
    lightProgram.bind();

    Program program(
            readFile("vertex.glsl"),
            readFile("fragment.glsl")
    );
    program.bind();

    Texture woodTexture{Image{"resources/wood.png"}};

    Framebuffer depthMapFB = Framebuffer::createDepthBufferOnly(SHADOW_WIDTH, SHADOW_HEIGHT, true);

    program.bind();
    program.setUniform("diffuseTexture", 0);
    program.setUniform("depthMap", 1);

    glm::vec3 lightPos(0.0f, 0.0f, 0.0f);

    while (!glfwWindowShouldClose(window)) {
        // Update time
        float currentFrame = glfwGetTime();
        deltaTime = currentFrame - lastFrame;
        lastFrame = currentFrame;

        // Handle camera controls
        handleKeyCameraControls(window);

        // Check errors
        int error;
        while ((error = glGetError())) {
            std::cout << "GL error: " << error << std::endl;
            glfwSetWindowShouldClose(window, GLFW_TRUE);
        }

        // move light position over time
        lightPos.z = sin(glfwGetTime() * 0.5) * 3.0;

        // 0. create depth cubemap transformation matrices
        // -----------------------------------------------
        float near_plane = 1.0f;
        float far_plane = 25.0f;
        glm::mat4 shadowProj = glm::perspective(glm::radians(90.0f), (float) SHADOW_WIDTH / (float) SHADOW_HEIGHT, near_plane, far_plane);
        std::vector<glm::mat4> shadowTransforms{
                shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(1.0f, 0.0f, 0.0f), glm::vec3(0.0f, -1.0f, 0.0f)),
                shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(-1.0f, 0.0f, 0.0f), glm::vec3(0.0f, -1.0f, 0.0f)),
                shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(0.0f, 1.0f, 0.0f), glm::vec3(0.0f, 0.0f, 1.0f)),
                shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(0.0f, -1.0f, 0.0f), glm::vec3(0.0f, 0.0f, -1.0f)),
                shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(0.0f, 0.0f, 1.0f), glm::vec3(0.0f, -1.0f, 0.0f)),
                shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(0.0f, 0.0f, -1.0f), glm::vec3(0.0f, -1.0f, 0.0f))
        };

        // Render to depth map
        glViewport(0, 0, SHADOW_WIDTH, SHADOW_HEIGHT);
        depthMapFB.bind();
        glClear(GL_DEPTH_BUFFER_BIT);
        depthMapProgram.bind();
        for (unsigned int i = 0; i < 6; ++i) {
            depthMapProgram.setUniform("shadowMatrices[" + std::to_string(i) + "]", shadowTransforms[i]);
        }
        depthMapProgram.setUniform("far_plane", far_plane);
        depthMapProgram.setUniform("lightPos", lightPos);
        renderScene(depthMapProgram);
        Framebuffer::bindDefault();

        // render scene as normal
        glViewport(0, 0, width, height);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
        program.bind();
        glm::mat4 projection = glm::perspective(glm::radians(camera.zoom()), (float) width / (float) height, 0.1f, 100.0f);
        glm::mat4 view = camera.viewMatrix();
        program.setUniform("projection", projection);
        program.setUniform("view", view);

        // set lighting uniforms
        program.setUniform("lightPos", lightPos);
        program.setUniform("viewPos", camera.position());
        program.setUniform("shadows", shadows); // enable/disable shadows by pressing 'SPACE'
        program.setUniform("far_plane", far_plane);
        glActiveTexture(GL_TEXTURE0);
        woodTexture.bind();
        glActiveTexture(GL_TEXTURE1);
        depthMapFB.texture().bind();

        renderScene(program);

        //Draw a cube in the light position
        lightProgram.bind();
        lightProgram.setUniform("projection", projection);
        lightProgram.setUniform("view", view);
        auto model = glm::mat4(1.0f);
        model = glm::translate(model, lightPos);
        model = glm::scale(model, glm::vec3(0.1f));
        lightProgram.setUniform("model", model);
        renderCube();

        glBindVertexArray(0);
        glfwSwapBuffers(window);
        glfwPollEvents();
    }

    glfwDestroyWindow(window);
    glfwTerminate();
}

// renders the 3D scene
// --------------------
void renderScene(const Program &program) {

    // room cube
    glm::mat4 model = glm::mat4(1.0f);
    model = glm::scale(model, glm::vec3(5.0f));
    program.setUniform("model", model);
    // note that we disable culling here since we render 'inside' the cube instead of the usual 'outside' which throws off the normal culling methods.
    glDisable(GL_CULL_FACE);
    // A small little hack to invert normals when drawing cube from the inside so lighting still works.
    program.setUniform("reverse_normals", 1);
    renderCube();
    program.setUniform("reverse_normals", 0); // and of course disable it
    glEnable(GL_CULL_FACE);

    // cubes
    model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(4.0f, -3.5f, 0.0));
    model = glm::scale(model, glm::vec3(0.5f));
    program.setUniform("model", model);
    renderCube();

    model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(2.0f, 3.0f, 1.0));
    model = glm::scale(model, glm::vec3(0.75f));
    program.setUniform("model", model);
    renderCube();

    model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(-3.0f, -1.0f, 0.0));
    model = glm::scale(model, glm::vec3(0.5f));
    program.setUniform("model", model);
    renderCube();

    model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(-1.5f, 1.0f, 1.5));
    model = glm::scale(model, glm::vec3(0.5f));
    program.setUniform("model", model);
    renderCube();

    model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(-1.5f, 2.0f, -3.0));
    model = glm::rotate(model, glm::radians(60.0f), glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
    model = glm::scale(model, glm::vec3(0.75f));
    program.setUniform("model", model);
    renderCube();
}

